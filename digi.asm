$INCLUDE(delay2.inc)
PUBLIC DIGI
_DIGI_ASM SEGMENT CODE
RSEG _DIGI_ASM
DIGI:
CLR P0.5
SETB P0.6
MOV A,R5                   ；接收外部编码
MOV R4,#0
MOV R5,A
DU21:
CLR P0.4
INC R4
MOV R5,A
RRC A
MOV P0.7,C
SETB P0.4
CJNE R4,#08H,DU21
AJMP DUO2
DUO2:
SETB P0.5
CLR P0.6
LCALL DELAY2
RET
                             ；其余同DIG函数
END
